public class SOF {

    public int ID;
    public String SOF;

    public SOF(int ID, String SOF) {
        this.ID = ID;
        this.SOF = SOF;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getSOF() {
        return SOF;
    }

    public void setSOF(String SOF) {
        this.SOF = SOF;
    }



}
