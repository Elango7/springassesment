public class GAI {

    public int ID;
    public String GAI;

    public GAI(int ID, String GAI) {
        this.ID = ID;
        this.GAI = GAI;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getGAI() {
        return GAI;
    }

    public void setGAI(String GAI) {
        this.GAI = GAI;
    }
}
